// Generates src/environments/deployment.ts with the per-instance deployment
// config baked into the web bundle at build time. The browser cannot read a
// `.env` at runtime, so the native (gloss) language has to be compiled in here.
//
// Resolution order for NATIVE_LANG: an explicit `process.env.NATIVE_LANG`
// (e.g. `NATIVE_LANG=en pnpm --filter web build`) wins; otherwise it is read
// from the deployment's `apps/api/.env` (the single per-instance config file);
// otherwise it defaults to 'nl'. Runs before `dev`/`build`/`watch` and after
// `install` (see package.json). The output file is gitignored.
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');

const SUPPORTED_NATIVE_LANGS = ['nl', 'en'];
const DEFAULT_NATIVE_LANG = 'nl';

// Minimal `.env` reader for a single key — avoids pulling in a dependency just
// to look up one variable. Handles `KEY=value`, surrounding quotes, comments,
// and blank lines; ignores everything else.
function readEnvVar(filePath, key) {
  if (!existsSync(filePath)) return undefined;
  for (const raw of readFileSync(filePath, 'utf8').split(/\r?\n/)) {
    const line = raw.trim();
    if (!line || line.startsWith('#')) continue;
    const eq = line.indexOf('=');
    if (eq === -1) continue;
    if (line.slice(0, eq).trim() !== key) continue;
    return line
      .slice(eq + 1)
      .trim()
      .replace(/^["']|["']$/g, '');
  }
  return undefined;
}

const fromEnv = process.env.NATIVE_LANG?.trim();
const fromFile = readEnvVar(resolve(root, '../api/.env'), 'NATIVE_LANG');
let nativeLang = fromEnv || fromFile || DEFAULT_NATIVE_LANG;

if (!SUPPORTED_NATIVE_LANGS.includes(nativeLang)) {
  throw new Error(
    `[generate-deployment] unsupported NATIVE_LANG "${nativeLang}"; ` +
      `expected one of ${SUPPORTED_NATIVE_LANGS.join(', ')}`,
  );
}

const source = fromEnv ? 'env' : fromFile ? 'apps/api/.env' : 'default';

const contents = `// AUTO-GENERATED by scripts/generate-deployment.mjs — do not edit.
// Regenerated before every \`dev\`/\`build\`; this file is gitignored.
export const deployment = {
  nativeLang: '${nativeLang}',
} as const;
`;

writeFileSync(resolve(root, 'src/environments/deployment.ts'), contents);
console.log(`[generate-deployment] nativeLang=${nativeLang} (from ${source})`);
